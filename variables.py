#----------------------Opérateurs d'affectation------------#
# =   égal à
# +=  addition et affectation
# -=  soustraction et affectation
# *=  multiplication et affectation
# /=  division et affectation
# %=  modulo et affectation
# **= exponentiation et affectation

# # Affectation
a = 5
# print(a)  # Résultat : 5

# # Affectation avec addition
a = 5
a += 2          #=> a = a + 2  
# print(a)  # Résultat : 7

# # Affectation avec soustraction
a = 5
a -= 2         #=> a = a - 2
# print(a)  # Résultat : 3

# # Affectation avec multiplication
a = 5
a *= 2     #=> a = a * 2
# print(a)  # Résultat : 10

# Affectation avec division
a = 5
a /= 2    #=> a = a / 2
# print(a)  # Résultat : 2.5

# Affectation avec modulo
a = 5
a %= 2    #=> a = a % 2
# print(a)  # Résultat : 1

# Affectation avec exponentiation
a = 2
a **= 3   #=> a = a ** 3
# print(a)  # Résultat : 8


